     <p>CAIR was established in 2011 with the aim of building world class Artificial Intelligence research capacity in South Africa. CAIR conducts foundational, directed and applied research into various aspects of AI through its nine established research groups: Adaptive and Cognitive Systems, AI and Cybersecurity, AI for Development, Applications of Machine Learning, Computational Logic, Ethics of AI, Foundations of Machine Learning, Knowledge Representation and Reasoning, and Probabilistic Modelling. The academics leading the individual research groups in CAIR are established researchers in their research focus areas, contributing to the advancement and thought leadership in the various disciplines constituting AI. CAIR also has two emerging research groups: Swarm Intelligence and Speech Technologies. </p>
